export function createTranslator(language) {
  const dict = strings[language] || strings.en;
  return (key) => dict[key] || strings.en[key] || key;
}

export function moduleLabel(name, language) {
  return moduleLabels[name]?.[language] || moduleLabels[name]?.en || name;
}

export function normalizeLanguage(value) {
  if (!value) return "en";
  if (value === "ar" || value === "Arabic") return "ar";
  return "en";
}
